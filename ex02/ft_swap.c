#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	c;
	
	c = *a;
	*a = *b;
	*b = c;
}

int	main(void)
{
	int* aptr;
	int* bptr;
	ft_swap(aptr, bptr);
	return(0);
	
}
