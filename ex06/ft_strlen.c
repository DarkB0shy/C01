#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return(i);
}

int	main(void)
{
	char	c[] = "Henlo";
	ft_strlen(c);	
}
